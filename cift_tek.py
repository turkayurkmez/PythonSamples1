# 2. Girilen sayı, int tipine çevrilmeli ve bir değişkene atılmalı
# 3. Bu değişken, 2'ye tam bölünüyorsa çift, bölünmüyorsa tektir.
print('bir sayı girin')
girilen_sayi = int(input())
